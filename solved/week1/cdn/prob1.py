# 특정 구간의 합
#
# N개의 원소로 이루어진 수열 A가 주어지고, M개의 두 정수쌍 a1, a2가 주어짐.
# M번에 걸쳐 수열 A의 a1번째 수부터 a2까지 합을 구해 출력하는 프로그램을 작성
# 단, 수열 A를 전역변수로 표현하여 문제를 해결

# 첫번째 줄에 N과 M이 공백을 사이에 두고 주어짐
# 두번째 줄에 N개의 원소가 공백을 사이에 두고 주어짐
# 세번째 줄부터 M개의 줄에 걸쳐 각 줄마다 두 정수쌍 a1, a2 정보가 공백을 사이에 두고 주어짐

# 첫번째줄부터 각 줄마다 두 정수 쌍 a1부터 a2까지의 합을 출력

# 예제 1

# 6 4
# 6 5 4 3 2 1
# 1 2
# 2 4
# 3 4
# 5 6

# 출력
# 11
# 12
# 7 
# 3

# 처음에 배열 인덱스로 접근해서 해당 구간 인덱스에 있는 값들을 for문을 이용해 합을 구하려고 하니
# 잘 구현이 안되어서 gpt짱에게 물어봄

n, m = map(int, input().split())
arr = list(map(int, input().split()))
queries = [tuple(map(int, input().split())) for _ in range(m)]

# 누적합 배열 생성
prefix = [0] * (n + 1)
for i in range(n):
    prefix[i + 1] = prefix[i] + arr[i]

def part_sum():
    for start, end in queries:
        print(prefix[end] - prefix[start - 1])

part_sum()

